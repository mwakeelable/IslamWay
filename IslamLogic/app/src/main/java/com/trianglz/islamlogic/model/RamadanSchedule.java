public class RamadanSchedule {

    private String date;
    private String suhur_time;
    private String iftar_time;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getSuhur_time() {
        return suhur_time;
    }

    public void setSuhur_time(String suhur_time) {
        this.suhur_time = suhur_time;
    }

    public String getIftar_time() {
        return iftar_time;
    }

    public void setIftar_time(String iftar_time) {
        this.iftar_time = iftar_time;
    }
}
